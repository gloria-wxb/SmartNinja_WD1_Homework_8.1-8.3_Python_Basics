mood = input("Tell me your mood: ")
if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Try to smile, it could help.")
elif mood == "excited":
    print("Sounds like an adventure is coming your way!")
elif mood == "relaxed":
    print("That's nice, keep it up!")
else:
    print("I don't recognize this mood")